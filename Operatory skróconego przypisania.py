
# suma += i to skórcony zapisu suma = suma + i "increment assignment operator" Działa tak:
# Pobiera obecną wartość zmiennej.
# Dodaje do niej wartość po prawej stronie.
# Przypisuje nową wartość do tej samej zmiennej.

#  Przykład
suma = 0 # Początkowa wartość sumy
suma += 5 # To samo co suma = suma + 5
print(f"Suma liczb: {suma}") # Wynik 5
suma += 3 # To samo co suma = suma + 3
print(f"Suma liczb: {suma}") # Wynik 8

# Inne operatory skróconego przypisania

# += Dodawanie
x = 10
x += 5
print(f"Suma liczb: {x}")

# -= Odejmowanie
x = 20
x -= 7
print(f"Rożnica liczb: {x}")

# *= Mnożenie
x = 3
x *= 4
print(f"Mnożenie liczb: {x}")

# /= Dzielenie
x = 10
x /= 2
print(f"Rożnica liczb: {x}")

# //= Dzielenie całkowite
x = 10
x //= 3
print(f"Rożnica liczb: {x}")

# %= Zachowuje resztę z dzelenia
x = 10
x %= 3
print(f"Rożnica liczb: {x}")

# **= Potęgowanie
x = 2
x **= 3
print(f"Rożnica liczb: {x}")

